public class C {
    public int xyz;
    public double yzx;
    public int zxy;

    public C(int xyz, double yzx, int zxy) {
        this.xyz = xyz;
        this.yzx = yzx;
        this.zxy = zxy;
    }

    public int getXyz() {
        return xyz;
    }

    public void setXyz(int xyz) {
        this.xyz = xyz;
    }

    public double getYzx() {
        return yzx;
    }

    public void setYzx(double yzx) {
        this.yzx = yzx;
    }

    public int getZxy() {
        return zxy;
    }

    public void setZxy(int zxy) {
        this.zxy = zxy;
    }
}
